package com.example.blooddonation;

public class adminAd {

    String work1, work2, work3;

    public adminAd() {
    }

    public adminAd(String work1, String work2, String work3) {
        this.work1 = work1;
        this.work2 = work2;
        this.work3 = work3;
    }

    public String getWork1() {
        return work1;
    }

    public void setWork1(String work1) {
        this.work1 = work1;
    }

    public String getWork2() {
        return work2;
    }

    public void setWork2(String work2) {
        this.work2 = work2;
    }

    public String getWork3() {
        return work3;
    }

    public void setWork3(String work3) {
        this.work3 = work3;
    }
}
